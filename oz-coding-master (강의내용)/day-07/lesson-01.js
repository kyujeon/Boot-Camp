// 첫 번째로, 위와 같은 형태에서는 expression(표현식)을 평가한다.
// 결과 값이 true와 같은 값이면, statement (실행문)을 실행한다.
// expression이 false와 같은 값이면, statement를 실행하지 않는다.

// 자바스크립트 문법은 if 키워드가 괄호를 둘러싼 표현식 뒤에 문 하나만 있어야 한다고 규정하지만,
// 문 블럭을 써서 여러 문을 하나로 모을 수도 있다.

if (!expression) {
  address = "";
  message = "정확한 주소를 입력하세요.";
}

// if 문의 두 번째 형태는 expression이 flase로 평가될 때, 실행하는 else 가 있다.
// 문법은 다음과 같다.

if (expression) {
  statement1;
} else {
  satement2;
}

// 이런 형태는 expression 이 true 와 같은 값이면 statement1을 실행하고,
// false 같은 값이면 statement2를 실행한다.

//동등 연산자 vs 일치 연산자
//동등 연산자 : 타입이 달라도 자동으로 변환되어 비교
//일치 연산자 : 타입과 값이 모두 같아야 true

console.log(1 == "1"); // true : 문자열 "1"이 숫자 1로 변환된다 (동등 연산자 : ==)
console.log(1 === "1"); // flase : 숫자와 문자 타입이 다름 number vs string (일치 연산자 : ===)

