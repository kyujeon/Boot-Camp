// *1.3 switch

// if 문은 프로그램 실행 흐름에 분기점을 만들고, else if를 써서 분기점을 여럿 만들 수 있다.
// 하지만 모든 분기점이 같은 표현식의 값에 좌우된다면, else if 가 최선의 선택이 아닐 수도 있다.
// => 코드상으로 낭비가 될 수 있다.
// 똑같은 표현식을 여러 if 문이 반복해 평가하는 것은 낭비다.

// switch 문으로 똑같은 상황에 대응할 수 있다.
// switch 문은 switch 키워드 뒤에 괄호를 들러싼 표현식을 쓰고 그 뒤에 중괄호로 코드 블록을 쓰는 형태이다.

// switch (expression) {
//   statements;
// }

// switch 문의 정확한 문법은 이보다 복잡하다.
// 코드 블록 곳곳에 case 키워드를 쓰고 그 뒤에 표현식과 콜론을 붙일 수 있다.

// switch 문이 실행되면 먼저 expression의 값을 계산한 후
// case 라벨의 표현식 중에서 expression과 같은 값으로 평가되는 것을 찾는다.

// 이때, "같은" 값은 === 연산자(일치 연산자)를 기준으로 판단한다.
// 일치하는 것을 찾으면 해당 case 코드 블록을 실행하기 시작한다.
// 일치하는 case를 찾지 못하면 default: 라벨을 찾는다.
// default: 라벨 조차 없으면 switch 문은 코드 블록 전체를 건너 뛴다. => code pass

switch (n) {
  case 1: // n === 1 이면 여기서 시작한다.
    // 코드 블록 #1을 실행한다.
    break; // 여기서 멈춘다.
  case 2: // n === 2 이면 여기서 시작한다.
    // 코드 블록 #2을 실행한다.
    break; // 여기서 멈춘다.
  case 3: // n === 3 이면 여기서 시작한다.
    // 코드 블록 #3을 실행한다.
    break; // 여기서 멈춘다.
  default: // 코드 블록 #4를 실행한다.
    break; // 여기서 멈춘다.
}

